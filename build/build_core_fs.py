"""
Embed every BRUT-V core/*.s file into a single JS module so the browser
assembler can resolve `.include "..."` without any network call.

Run: python build/build_core_fs.py
Writes to: ../core-fs.js
"""

import os
import json

HERE       = os.path.dirname(os.path.abspath(__file__))
CORE_DIR   = os.path.normpath(os.path.join(HERE, "..", "..", "core"))
OUT_PATH   = os.path.normpath(os.path.join(HERE, "..", "core-fs.js"))

def main():
    files = {}
    for name in sorted(os.listdir(CORE_DIR)):
        if not name.endswith(".s"):
            continue
        with open(os.path.join(CORE_DIR, name), "r", encoding="utf-8") as f:
            files[name] = f.read()

    body = ",\n  ".join(f"{json.dumps(name)}: {json.dumps(content)}"
                       for name, content in files.items())

    js = (
        "// AUTO-GENERATED by build/build_core_fs.py — do not edit by hand.\n"
        "// Embeds every file in BRUT-V/core/ as a string. The assembler reads\n"
        "// `.include` paths from this map so the framework runs offline.\n"
        "export const CORE_FILES = {\n"
        f"  {body}\n"
        "};\n"
    )
    with open(OUT_PATH, "w", encoding="utf-8") as f:
        f.write(js)
    total = sum(len(c) for c in files.values())
    print(f"Wrote {OUT_PATH}: {len(files)} files, {total} bytes total.")

if __name__ == "__main__":
    main()
