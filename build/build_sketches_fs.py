"""
Embed every BRUT-V sketches/*.asm into a single JS module so the browser can
load them without a network round-trip. We exclude sketches that RARS itself
rejects (duplicate labels, backward .data) — they wouldn't run anyway.

Run: python build/build_sketches_fs.py
Writes to: ../sketches-fs.js
"""

import os
import json

HERE         = os.path.dirname(os.path.abspath(__file__))
SKETCHES_DIR = os.path.normpath(os.path.join(HERE, "..", "..", "sketches"))
OUT_PATH     = os.path.normpath(os.path.join(HERE, "..", "sketches-fs.js"))

# Sketches that don't assemble in RARS either — kept on disk for archival but
# never shipped to the browser, since they'd produce a confusing error.
EXCLUDED = {
    "riscv1.asm",                      # .data 0x10009000 backwards + duplicate label
    "sketch_dfs_8.asm",                # duplicate label "loop"
    "sketch_rotated_triangles.asm",    # duplicate label "loop"
}

# Display names shown in the sidebar. Filenames are kept short and ASCII; the
# UI label is what the visitor actually reads. Order of insertion here is the
# order shown in the sidebar.
DISPLAY_NAMES = {
    "recursive_grid.asm":      "Recursive Grid",
    "primitives.asm":          "Primitives",
    "arc.asm":                 "Arc",
    "polygon_manual.asm":      "Polygon (manual)",
    "polygon_automatic.asm":   "Polygon (automatic)",
    "random_polygon.asm":      "Random Polygon",
    "gradient.asm":            "Gradient",
    "flags.asm":               "Flags",
    "text.asm":                "Text",
    "noise_pixels.asm":        "Noise2d",
    "noise_ring.asm":          "Blob",
    "noise_ring_animated.asm": "Living Blob",
    "bouncing_circle.asm":     "Bouncing Ball",
    "rotating_cube.asm":       "Rotating Cube",
    "terrain_3d.asm":          "Noise Terrain 3D",
    "automaton.asm":           "Automaton",
    "maze.asm":                "Maze",
    "maze_unicursal.asm":      "Maze (unicursal)",
    "dubins_stamp.asm":        "Rubber Bands",
}

def main():
    files = {}
    for name in DISPLAY_NAMES.keys():     # iterate in declared display order
        if name in EXCLUDED:
            continue
        path = os.path.join(SKETCHES_DIR, name)
        if not os.path.exists(path):
            print(f"WARN: missing sketch on disk: {name}")
            continue
        with open(path, "r", encoding="utf-8") as f:
            files[name] = f.read()

    body = ",\n  ".join(f"{json.dumps(name)}: {json.dumps(content)}"
                       for name, content in files.items())
    display_body = ",\n  ".join(f"{json.dumps(n)}: {json.dumps(d)}"
                                for n, d in DISPLAY_NAMES.items() if n in files)

    js = (
        "// AUTO-GENERATED by build/build_sketches_fs.py — do not edit by hand.\n"
        "// Embeds shipped sketches as strings so they do not need separate fetches.\n"
        "// The app still loads Monaco from a CDN unless it is vendored separately.\n"
        "export const SKETCH_FILES = {\n"
        f"  {body}\n"
        "};\n"
        "\n"
        "// Pretty names shown in the sidebar; ordering matches insertion order.\n"
        "export const SKETCH_DISPLAY_NAMES = {\n"
        f"  {display_body}\n"
        "};\n"
        "\n"
        "// Sorted insertion-order list for the sidebar.\n"
        f"export const SKETCH_NAMES = {json.dumps(list(files.keys()), indent=2)};\n"
    )
    with open(OUT_PATH, "w", encoding="utf-8") as f:
        f.write(js)
    total = sum(len(c) for c in files.values())
    print(f"Wrote {OUT_PATH}: {len(files)} sketches, {total} bytes total.")

if __name__ == "__main__":
    main()
